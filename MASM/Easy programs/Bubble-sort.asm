ASSUME DS:DATA,CS:CODE

DATA SEGMENT
	
	MS1 DB 13,10, "ENTER THE NUMBER OF ELEMENTS: $"
	MS2 DB 13,10, "ENTER THE NUMBERS: $"
	MS3 DB 13,10, "SORTED ORDER: $"
	ARR DB 09H DUP(0)
	
DATA ENDS

CODE SEGMENT
	START:	
		MOV AX,DATA
		MOV DS,AX
		
		LEA DX,MS1
		MOV AH,09H
		INT 21H
		
		MOV AH,01H
		INT 21H
		SUB AL,'0'
		MOV BH,AL
		MOV BL,BH
		
		
		LEA DX,MS2
		MOV AH,09H
		INT 21H
		MOV SI,OFFSET ARR
		
READ:		MOV AH,01H
		INT 21H
		MOV [SI],AL
		INC SI
		DEC BL
		JNZ READ
		MOV BL,CH



OUTER:		 
		MOV SI,OFFSET ARR
		MOV CL,BH
		
INNER:	
			MOV AL,[SI]
			MOV AH,[SI+1]
			
			CMP AL,AH
			JL NO_SWAP
			  
SWAP:		
		MOV [SI],AH
		MOV [SI+1],AL
		
NO_SWAP:	INC SI
		DEC CL
		JNZ INNER
		DEC BL
		JNZ OUTER	
		
PRINT:		LEA DX,MS3
		MOV AH,09H
		INT 21H		
		MOV SI,OFFSET ARR
		MOV CL,BH
		
REP1:		MOV DL,[SI]
		MOV AH,02H
		INT 21H
		INC SI
		DEC CL
		JNZ REP1
		
		MOV AH,4CH
		INT 21H 
	 
	 
	CODE ENDS
	END START
