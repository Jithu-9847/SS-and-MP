ASSUME CS:CODE,DS:DATA

DATA SEGMENT

MS1 DB 13,10, "ENTER THE STRING $"
MS2 DB 13,10,"THE NUMBER OF VOWELS IS: $"
VOWEL DB 'a', 'A', 'e', 'E', 'i', 'I', 'o', 'O', 'u', 'U', '$'
 
DATA ENDS

CODE SEGMENT
START:	MOV AX,DATA
	MOV DS,AX
	
	LEA DX,MS1
	MOV AH,09H
	INT 21H
	MOV BL,00H
	
CHECK:	MOV AH,01H
	INT 21H
	CMP AL,0DH
	JZ PRINT
	LEA SI,VOWEL
	 
	
VOWEL_CHECK:	
	 
	CMP [SI],'$'     
	JZ CHECK       
	CMP AL,[SI]     
	JZ FOUND       
	INC SI         
	JMP VOWEL_CHECK
		
FOUND:	INC BL
	JMP CHECK
	
PRINT:	LEA DX,MS2
	MOV AH,09H
	INT 21H
	
	ADD BL,30H
	MOV DL,BL
	
	MOV AH,02H
	INT 21H
	JMP FINISH
	
FINISH:	MOV AH,4CH
	INT 21H
	 

CODE ENDS
END START
